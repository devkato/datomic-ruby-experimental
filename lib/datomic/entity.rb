# -*- coding: utf-8 -*-

module Datomic
  class Entity

    # ----------------------------------------------------------------------
    # list attributes
    # ----------------------------------------------------------------------
    def keySet
    end

    # ----------------------------------------------------------------------
    # aliases of 'keySet'
    # ----------------------------------------------------------------------
    def keys
      return self.keySet
    end

    # ----------------------------------------------------------------------
    # list values
    # ----------------------------------------------------------------------
    def values
    end

    # ----------------------------------------------------------------------
    # get the attribute specified for the entity
    # ----------------------------------------------------------------------
    def get(attribute)
      database, attr = attribute.split(/\//)

      puts %!database => #{database}!
      puts %!attr     => #{attr}!
    end

    # ----------------------------------------------------------------------
    # get the entity from index or storage server.
    # ----------------------------------------------------------------------
    def self.get(ident)
      return Entity.new(ident)
    end
  end
end
